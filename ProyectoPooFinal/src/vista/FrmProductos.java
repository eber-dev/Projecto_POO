    public void agregarItem(List<producto> item){
        for(producto p: item){
            switch(p.getId_categoria()){
                case 1:
                    Laptop.addItem(p.getNombre());
                    break;
                case 2:
                    Pc.addItem(p.getNombre());
                    break;
                case 3:
                    Componentes.addItem(p.getNombre());
                    break;
                case 4:
                    Perifericos.addItem(p.getNombre());
                    break;
                case 5:
                    Celulares.addItem(p.getNombre());
                    break;
                case 6:
                    Accesorios.addItem(p.getNombre());
                    break;
                case 7:
                    Redes.addItem(p.getNombre());
                    break;
            }
        }
    }
    
    public void limitarspiner(JSpinner[] j){
        for(int i=0; i<j.length;i++){
            SpinnerNumberModel modelo = new SpinnerNumberModel(1, 1, 10, 1);
            j[i].setModel(modelo);
        }
        
    }


